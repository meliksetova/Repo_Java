package Homework;

public class Basics {
    public static void main (String[] args) {
        int d = 5;
        System.out.println(d++);  //5
        System.out.println(d++);   //6
        System.out.println(++d);  //8
        System.out.println(d);    //8
        System.out.println(--d);  //7
        System.out.println(d);    //7
        System.out.println(d++);   //7
    }    }

